PixelLoopManager:
  push {lr}
  ldr r0, =0xF00F
  bl FillScreen
  bl DrawBlock_test
  pop {lr}
  mov pc, lr


//r0 - start x
//r1 - start y
//r2 - address of picture
DrawBlock:
  push {r4 - r8, lr}

  row_start       .req r10
  column_start    .req r9
  picture_address .req r8

  column_counter  .req r4
  row_counter     .req r5

  column_end      .req r6
  row_end         .req r7

  mov picture_address,   r2
  mov column_start,   r1
  mov row_start,  r0

  //row/column end = start + 32
  add row_end, row_start, #32
  add column_end, column_start, #32

  //column_counter = start -1 (because of preincrement)
  add column_counter, column_start, #-1
  //row_counter = start (ex. row_start = 0, row_counter = 0)
  mov row_counter,    row_start

  big_draw_block:
    cmp column_counter, column_end
    beq done_draw_block

    add column_counter, #1

    mov row_counter, row_start
  start_draw_block:
    cmp row_counter, row_end
    beq big_draw_block

    mov r0, row_counter
    mov r1, column_counter
    ldrh r2, [picture_address], #2
    bl DrawPixel
    add row_counter, #1

    b start_draw_block

  done_draw_block:

    .unreq column_counter
    .unreq row_counter

    .unreq row_start
    .unreq column_start

    .unreq row_end
    .unreq column_end

    .unreq picture_address

    pop {r4 - r8, lr}
    mov pc, lr


