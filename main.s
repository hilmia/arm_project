//r0 - Contains colour to fill screen with
FillScreen:
  push {r4 - r10, lr}

  colour .req r10
  row_size .req r6
  column_size .req r7
  row_counter	.req	r4
  column_counter	.req	r5


  mov row_size, #768
  mov column_size, #1024

  mov colour, r0

  mov row_counter, #-1

  fill_loop_row:
    cmp row_counter, row_size
    bge fill_done_block

    mov column_counter, #0

    add row_counter, #1


    fill_loop_column:
      cmp column_counter, column_size
      bge fill_loop_row

      mov r2, colour
      mov r0, column_counter
      mov r1, row_counter
      bl DrawPixel

      add column_counter, #1
      b fill_loop_column

  fill_done_block:
    .unreq row_size
    .unreq column_size
    .unreq row_counter
    .unreq column_counter

    pop {r4 - r10, lr}
    mov pc, lr



/* Draw Pixel
 *  r0 - x
 *  r1 - y
 *  r2 - color
 */

DrawPixel:
	push	{r0, r1, r2, r4, lr}


	offset	.req	r4

	// offset = (y * 1024) + x = x + (y << 10)
	add		offset,	r0, r1, lsl #10
	// offset *= 2 (for 16 bits per pixel = 2 bytes per pixel)
	lsl		offset, #1

	// store the colour (half word) at framebuffer pointer + offset

	ldr	r0, =FrameBufferPointer
	ldr	r0, [r0]
	strh	r2, [r0, offset]

  .unreq offset
	pop		{r0, r1, r2, r4, lr}
	mov pc, lr




